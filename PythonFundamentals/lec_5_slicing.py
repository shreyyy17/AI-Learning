
# Slicing


friends = ["rolf","xolf","eolf","golf","folf","dolf"]

print(friends[2:]) # here friends[start:end]
print(friends[2:5]) # here friends[start:end]
print(friends[-3:]) # here friends[start:end]
print(friends[-2:4]) # here friends[start:end]
print(friends[:]) # duplicated the list 



